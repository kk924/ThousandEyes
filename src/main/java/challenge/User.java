/**
 * Person table equivalent
 */
public class User {

    private Integer id;
    private String name;

    protected Integer getId() {
        return id;
    }

    protected void setId(Integer id) {
        this.id = id;
    }

    protected String getName() {
        return name;
    }

    protected void setName(String name) {
        this.name = name;
    }


}


